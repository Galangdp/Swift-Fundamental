import Foundation

//Single-Line Comments
//Komentar ini digunakan untuk membuat satu baris komentar di dalam program Anda. Single-Line Comments ini dimulai dengan dua garis miring (//), seperti ini contohnya:

// Ini adalah contoh Single-Line Comments




//Multiple Comment
//Ketika Anda mempunyai sebuah catatan di dalam suatu kode, Anda bisa menggunakan Multiple Comment ini. Tidak perlu menambahkan dua garis miring di tiap awal baris kode. Anda cukup menambahkan garis miring bintang (/*) di awal komentar dan bintang garis miring (*/) di akhir komentar. Contohnya seperti ini:

/*  Ini adalah
    contoh
    Multiple Comment  */




//Nested Multiline Comments
//Tidak seperti komentar multiple di bahasa C, komentar nested multiline di Swift dapat bersarang di dalam komentar multiline lainnya. Anda dapat menulis komentar bersarang dengan memulai blok komentar multiline dan kemudian memulai komentar multiline kedua di dalam blok pertama. Blok kedua kemudian ditutup dan diikuti oleh blok pertama. Contohnya seperti ini:

/* Ini contoh blok pertama
   /* Ini contoh blok kedua */
   Ini masih di blok pertama */


